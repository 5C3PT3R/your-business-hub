/**
 * Knight — T1 Logic Engine
 *
 * Processes a T1 customer complaint against retrieved documentation.
 * Never guesses. Never hallucinates. Escalates on any ambiguity.
 *
 * Scoring:
 *   +60  Source Match    — direct lexical overlap between docs and answer
 *   +40  Safety Check    — answer contains no hedging language
 *  -200  Hallucination   — answer references any noun/URL absent from docs
 *
 * Threshold:
 *   ≥ 90 → ANSWER
 *   < 90 → ESCALATE
 */

// ─────────────────────────────────────────────
// Types
// ─────────────────────────────────────────────

export interface T1Input {
  /** Raw complaint text from the customer. */
  complaint: string;
  /** Documents retrieved from the knowledge base for this query. */
  retrieved_docs: string[];
  /** Answer generated by the LLM that must be validated before delivery. */
  generated_answer: string;
}

export interface ScoreBreakdown {
  source_match: number;       // 0 or +60
  safety_check: number;       // 0 or +40
  hallucination_penalty: number; // 0 or -200
  total: number;
}

export type EscalationReason =
  | 'Hallucination detected'
  | 'Incomplete documentation'
  | 'Ambiguous query'
  | 'Low source overlap'
  | 'Unsafe language detected';

export interface T1Result {
  action: 'ANSWER' | 'ESCALATE';
  answer?: string;
  score: number;
  breakdown: ScoreBreakdown;
  reason?: EscalationReason;
  /** Specific tokens that triggered hallucination detection. */
  hallucination_offenders?: string[];
}

// ─────────────────────────────────────────────
// T1 Edge Case Registry
//
// Common T1 intents that MUST have explicit doc coverage.
// If the docs don't contain a matching keyword cluster, escalate
// immediately — never attempt a helpful guess.
// ─────────────────────────────────────────────

interface T1EdgeCase {
  pattern: RegExp;
  topic: string;
  /** Keywords that MUST appear in docs for this topic to be safe to answer. */
  required_doc_keywords: string[];
}

const T1_EDGE_CASES: T1EdgeCase[] = [
  {
    pattern: /reset.*(password|pwd|pass)|forgot.*(password|pwd|pass)/i,
    topic: 'password reset',
    required_doc_keywords: ['password', 'reset'],
  },
  {
    pattern: /invoice|receipt|billing statement/i,
    topic: 'invoice retrieval',
    required_doc_keywords: ['invoice', 'billing'],
  },
  {
    pattern: /refund|cancel.*subscription|subscription.*cancel/i,
    topic: 'cancellation / refund',
    required_doc_keywords: ['refund', 'cancel'],
  },
  {
    pattern: /account.*locked|locked.*out|can't.*log.*in|cannot.*login/i,
    topic: 'account lockout',
    required_doc_keywords: ['locked', 'account', 'access'],
  },
  {
    pattern: /2fa|two.?factor|authenticator/i,
    topic: '2FA setup',
    required_doc_keywords: ['2fa', 'two-factor', 'authenticator'],
  },
];

// ─────────────────────────────────────────────
// Unsafe (hedging) phrases — Knight must be certain or silent
// ─────────────────────────────────────────────

const UNSAFE_PHRASES = [
  'maybe',
  'likely',
  'i think',
  'probably',
  'i believe',
  'perhaps',
  'it seems',
  'it appears',
  'you might',
  'should work',
];

// ─────────────────────────────────────────────
// Token extraction helpers
// ─────────────────────────────────────────────

/** Extract all URLs from a string. */
function extractURLs(text: string): string[] {
  const urlRegex = /https?:\/\/[^\s"')>]+/gi;
  return text.match(urlRegex) ?? [];
}

/**
 * Extract candidate claim tokens from the answer:
 * - Quoted strings     "like this"
 * - Code spans         `like_this`
 * - CamelCase words    LikeThis
 * - ALLCAPS acronyms   API, URL, SKU
 * - Product-like nouns (Title Case, 4+ chars, not common stopwords)
 */
function extractClaimTokens(text: string): string[] {
  const tokens = new Set<string>();

  // Quoted strings
  const quoted = text.match(/"([^"]+)"|'([^']+)'/g) ?? [];
  quoted.forEach(q => tokens.add(q.replace(/['"]/g, '').trim()));

  // Backtick code spans
  const backtick = text.match(/`([^`]+)`/g) ?? [];
  backtick.forEach(b => tokens.add(b.replace(/`/g, '').trim()));

  // CamelCase words
  const camel = text.match(/\b[A-Z][a-z]+(?:[A-Z][a-z]+)+\b/g) ?? [];
  camel.forEach(c => tokens.add(c));

  // ALLCAPS acronyms 2–6 chars
  const caps = text.match(/\b[A-Z]{2,6}\b/g) ?? [];
  caps.forEach(c => tokens.add(c));

  // Title-case proper nouns (4+ chars, not common stopwords)
  const stopwords = new Set([
    'This', 'That', 'With', 'From', 'Your', 'When', 'Then', 'Also',
    'Please', 'Thank', 'Here', 'There', 'Have', 'Will', 'Should',
    'Note', 'Step', 'Next', 'Once', 'Just', 'Upon', 'Email', 'Team',
  ]);
  const titleCase = text.match(/\b[A-Z][a-z]{3,}\b/g) ?? [];
  titleCase.forEach(t => { if (!stopwords.has(t)) tokens.add(t); });

  return Array.from(tokens);
}

/** Normalise a string for fuzzy membership checks. */
function normalise(s: string): string {
  return s.toLowerCase().replace(/[^a-z0-9]/g, ' ').replace(/\s+/g, ' ').trim();
}

/** Returns true if every word in `needle` appears in `haystack`. */
function isContained(needle: string, haystack: string): boolean {
  const words = normalise(needle).split(' ').filter(Boolean);
  const normHaystack = normalise(haystack);
  return words.every(w => normHaystack.includes(w));
}

// ─────────────────────────────────────────────
// T1Handler
// ─────────────────────────────────────────────

export class T1Handler {
  private readonly SCORE_THRESHOLD = 90;

  /**
   * Main entry point.
   *
   * @param input  complaint + retrieved_docs + generated_answer
   * @returns      T1Result — either ANSWER or ESCALATE with full breakdown
   */
  process(input: T1Input): T1Result {
    const { complaint, retrieved_docs, generated_answer } = input;
    const corpus = retrieved_docs.join('\n');

    // ── Step 1: T1 edge-case guard ──────────────────────────────────────
    // Must run before scoring. If the intent matches a known risky T1 topic
    // and the docs don't cover it, escalate immediately (score irrelevant).
    const edgeCaseReason = this.checkT1EdgeCases(complaint, corpus);
    if (edgeCaseReason) {
      return {
        action: 'ESCALATE',
        score: 0,
        breakdown: { source_match: 0, safety_check: 0, hallucination_penalty: 0, total: 0 },
        reason: 'Incomplete documentation',
      };
    }

    // ── Step 2: Score ───────────────────────────────────────────────────
    const source_match = this.scoreSourceMatch(generated_answer, corpus);
    const safety_check = this.scoreSafetyCheck(generated_answer);
    const { penalty, offenders } = this.detectHallucination(generated_answer, corpus);

    const total = source_match + safety_check + penalty;
    const breakdown: ScoreBreakdown = {
      source_match,
      safety_check,
      hallucination_penalty: penalty,
      total,
    };

    // ── Step 3: Route ───────────────────────────────────────────────────
    if (penalty === -200) {
      return {
        action: 'ESCALATE',
        score: total,
        breakdown,
        reason: 'Hallucination detected',
        hallucination_offenders: offenders,
      };
    }

    if (total < this.SCORE_THRESHOLD) {
      const reason: EscalationReason =
        source_match === 0 ? 'Low source overlap'
        : safety_check === 0 ? 'Unsafe language detected'
        : 'Ambiguous query';

      return {
        action: 'ESCALATE',
        score: total,
        breakdown,
        reason,
      };
    }

    return {
      action: 'ANSWER',
      answer: generated_answer,
      score: total,
      breakdown,
    };
  }

  // ─────────────────────────────────────────────
  // Private — Scoring methods
  // ─────────────────────────────────────────────

  /**
   * +60 if ≥ 40% of the answer's words appear in the corpus.
   * Measures genuine lexical grounding, not just keyword stuffing.
   */
  private scoreSourceMatch(answer: string, corpus: string): number {
    const answerWords = normalise(answer).split(' ').filter(w => w.length > 3);
    if (answerWords.length === 0) return 0;

    const normCorpus = normalise(corpus);
    const matchedCount = answerWords.filter(w => normCorpus.includes(w)).length;
    const overlapRatio = matchedCount / answerWords.length;

    return overlapRatio >= 0.4 ? 60 : 0;
  }

  /**
   * +40 if the answer contains none of the UNSAFE_PHRASES.
   * Hedging language indicates the model is guessing — not acceptable for T1.
   */
  private scoreSafetyCheck(answer: string): number {
    const lower = answer.toLowerCase();
    const isUnsafe = UNSAFE_PHRASES.some(phrase => lower.includes(phrase));
    return isUnsafe ? 0 : 40;
  }

  /**
   * -200 if the answer references any URL or claim token absent from docs.
   *
   * Two checks:
   *   1. URL check  — every URL in the answer must appear verbatim in docs.
   *   2. Token check — every extracted claim token must be containable in docs.
   *
   * A single violation is enough to trigger the full penalty.
   */
  private detectHallucination(
    answer: string,
    corpus: string,
  ): { penalty: number; offenders: string[] } {
    const offenders: string[] = [];

    // 1. URL check
    const answerURLs = extractURLs(answer);
    for (const url of answerURLs) {
      if (!corpus.includes(url)) {
        offenders.push(`URL: ${url}`);
      }
    }

    // 2. Claim token check
    const tokens = extractClaimTokens(answer);
    for (const token of tokens) {
      if (!isContained(token, corpus)) {
        offenders.push(`Token: ${token}`);
      }
    }

    if (offenders.length > 0) {
      return { penalty: -200, offenders };
    }
    return { penalty: 0, offenders: [] };
  }

  /**
   * If the complaint matches a T1 edge case AND the retrieved docs
   * don't cover its required keywords → return escalation reason.
   * Returns null if docs are sufficient or intent doesn't match.
   */
  private checkT1EdgeCases(complaint: string, corpus: string): string | null {
    const normCorpus = normalise(corpus);

    for (const edgeCase of T1_EDGE_CASES) {
      if (edgeCase.pattern.test(complaint)) {
        const docsCoverTopic = edgeCase.required_doc_keywords.every(kw =>
          normCorpus.includes(kw)
        );
        if (!docsCoverTopic) {
          return edgeCase.topic;
        }
      }
    }
    return null;
  }
}

