declare const __runTests: boolean | undefined;
if (typeof __runTests !== 'undefined' && __runTests) {
  const handler = new T1Handler();

  const RESET_DOC = `
    Password Reset: To reset your password, go to the login page and click
    "Forgot Password". Enter your registered email address. You will receive
    a reset link valid for 30 minutes. Click the link and choose a new password.
    Passwords must be at least 8 characters.
  `;

  const INVOICE_DOC = `
    Invoices: All invoices are available in the Billing section of your account
    dashboard. Navigate to Settings → Billing → Invoices. You can download a PDF
    copy of any invoice from the last 24 months.
  `;

  const tests: Array<{
    name: string;
    input: T1Input;
    expected_action: 'ANSWER' | 'ESCALATE';
  }> = [
    {
      name: '✅ Test 1 — Grounded answer (should ANSWER)',
      input: {
        complaint: 'How do I reset my password?',
        retrieved_docs: [RESET_DOC],
        generated_answer:
          'To reset your password, go to the login page and click "Forgot Password". ' +
          'Enter your registered email and you will receive a reset link valid for 30 minutes. ' +
          'Click the link and choose a new password of at least 8 characters.',
      },
      expected_action: 'ANSWER',
    },
    {
      name: '❌ Test 2 — Fabricated URL not in docs (should ESCALATE: Hallucination)',
      input: {
        complaint: 'Where is my invoice?',
        retrieved_docs: [INVOICE_DOC],
        generated_answer:
          'You can download your invoice from https://billing.acme-fake-portal.com/invoices. ' +
          'Navigate to Settings → Billing → Invoices in your account dashboard.',
      },
      expected_action: 'ESCALATE',
    },
    {
      name: '❌ Test 3 — Hedging language (should ESCALATE: Unsafe language)',
      input: {
        complaint: 'How do I get my invoice?',
        retrieved_docs: [INVOICE_DOC],
        generated_answer:
          'Invoices are likely available in your billing section. ' +
          'I think you can navigate to Settings to find them.',
      },
      expected_action: 'ESCALATE',
    },
    {
      name: '❌ Test 4 — Password reset with NO supporting docs (should ESCALATE: Incomplete docs)',
      input: {
        complaint: 'How do I reset my password?',
        retrieved_docs: ['General product FAQ. Contact us at support@example.com.'],
        generated_answer: 'You can reset your password from the login screen.',
      },
      expected_action: 'ESCALATE',
    },
    {
      name: '✅ Test 5 — Invoice with full docs, clean answer (should ANSWER)',
      input: {
        complaint: 'Where is my invoice?',
        retrieved_docs: [INVOICE_DOC],
        generated_answer:
          'All invoices are available in the Billing section of your account dashboard. ' +
          'Navigate to Settings → Billing → Invoices to download a PDF copy.',
      },
      expected_action: 'ANSWER',
    },
  ];

  let passed = 0;

  for (const test of tests) {
    const result = handler.process(test.input);
    const ok = result.action === test.expected_action;
    if (ok) passed++;

    console.log(`\n${test.name}`);
    console.log(`  Expected : ${test.expected_action}`);
    console.log(`  Got      : ${result.action}  ${ok ? '✓' : '✗ FAILED'}`);
    console.log(`  Score    : ${result.breakdown.total} (match:${result.breakdown.source_match} safety:${result.breakdown.safety_check} penalty:${result.breakdown.hallucination_penalty})`);
    if (result.reason) console.log(`  Reason   : ${result.reason}`);
    if (result.hallucination_offenders?.length) {
      console.log(`  Offenders: ${result.hallucination_offenders.join(', ')}`);
    }
  }

  console.log(`\n─────────────────────────────`);
  console.log(`Results: ${passed}/${tests.length} passed`);
  console.log(`─────────────────────────────`);
}
